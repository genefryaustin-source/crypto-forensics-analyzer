def generate_sar_xml(
    case_id: str,
    filing_institution: str,
    ein: str,
    subject_addresses: List[str],
    total_volume: float,
    narrative: str,
    activity_date_start: str,
    activity_date_end: str,
) -> str:
    """
    Generate FinCEN SAR XML in BSA E-Filing format.
    Schema: FinCEN CTR/SAR XML v2.0 (simplified — real filing requires
    FinCEN-issued credentials and the full schema).
    """
    root = ET.Element("FinCEN_SAR", {
        "xmlns": "FinCEN/SAR/2.0",
        "version": "2.0",
        "created": datetime.now().isoformat(),
    })

    # Filing institution
    filer = ET.SubElement(root, "FilingInstitution")
    ET.SubElement(filer, "Name").text = filing_institution
    ET.SubElement(filer, "EIN").text = ein
    ET.SubElement(filer, "FilingDate").text = datetime.now().strftime("%Y%m%d")

    # Activity
    activity = ET.SubElement(root, "SuspiciousActivity")
    ET.SubElement(activity, "CaseID").text = case_id
    ET.SubElement(activity, "ActivityDateFrom").text = activity_date_start
    ET.SubElement(activity, "ActivityDateTo").text = activity_date_end
    ET.SubElement(activity, "TotalAmount").text = f"{total_volume:.2f}"
    ET.SubElement(activity, "Currency").text = "USD"
    ET.SubElement(activity, "VirtualCurrencyInvolved").text = "Y"

    # Subject addresses
    subjects = ET.SubElement(activity, "Subjects")
    for addr in subject_addresses[:10]:
        subj = ET.SubElement(subjects, "Subject")
        ET.SubElement(subj, "VirtualCurrencyAddress").text = addr
        ET.SubElement(subj, "AddressType").text = (
            "TRON" if addr.startswith("T") and len(addr) == 34 else
            "BITCOIN" if addr.startswith(("1","3","bc1")) else "EVM"
        )

    # Narrative
    ET.SubElement(activity, "Narrative").text = narrative

    # Indicators
    indicators = ET.SubElement(activity, "SuspiciousActivityIndicators")
    ET.SubElement(indicators, "VirtualCurrencyTransaction").text = "Y"
    ET.SubElement(indicators, "RapidMovementOfFunds").text = "Y"

    pretty = parseString(ET.tostring(root, encoding="unicode")).toprettyxml(indent="  ")
    return pretty


def generate_ctr_data(
    filer_name: str,
    filer_ein: str,
    transactions: List[Dict],
) -> Dict:
    """
    Generate Currency Transaction Report (CTR) data.
    Required for cash/crypto transactions ≥ $10,000.
    """
    total = sum(t.get("amount", 0) for t in transactions)
    return {
        "form_type":    "FinCEN CTR (Form 112)",
        "filing_date":  datetime.now().strftime("%Y-%m-%d"),
        "filer":        filer_name,
        "ein":          filer_ein,
        "total_amount": total,
        "tx_count":     len(transactions),
        "exceeds_threshold": total >= 10_000,
        "transactions": transactions[:25],
        "note": "CTR must be filed within 15 days of transaction via BSA E-Filing.",
    }


def generate_sar_pdf(
    narrative: str,
    case_id: str,
    subject_addresses: List[str],
    total_volume: float,
    filing_institution: str,
) -> io.BytesIO:
    """Generate a formatted SAR PDF document."""
    buf = io.BytesIO()
    doc = SimpleDocTemplate(buf, pagesize=letter,
                             topMargin=0.75*inch, bottomMargin=0.75*inch,
                             leftMargin=0.75*inch, rightMargin=0.75*inch)
    S = getSampleStyleSheet()
    tit  = ParagraphStyle("T", parent=S["Title"],   fontSize=14, spaceAfter=4)
    h1   = ParagraphStyle("H", parent=S["Heading1"],fontSize=11, spaceBefore=10)
    body = ParagraphStyle("B", parent=S["Normal"],   fontSize=8,  leading=13)
    code = ParagraphStyle("C", parent=S["Code"],     fontSize=7,  leading=10, fontName="Courier")
    warn = ParagraphStyle("W", parent=S["Normal"],   fontSize=8,  textColor=colors.HexColor("#ff4444"))

    elems = []
    elems.append(Paragraph("SUSPICIOUS ACTIVITY REPORT (SAR)", tit))
    elems.append(Paragraph(
        f"Case: {case_id}  |  Date: {datetime.now().strftime('%Y-%m-%d')}  |  "
        f"Filing Institution: {filing_institution or 'N/A'}",
        ParagraphStyle("sub", parent=S["Normal"], fontSize=8, textColor=colors.grey)
    ))
    elems.append(HRFlowable(width="100%", thickness=2, color=colors.HexColor("#ff4444"), spaceAfter=8))

    elems.append(Paragraph("⚠️ DRAFT — REQUIRES BSA OFFICER REVIEW BEFORE SUBMISSION", warn))
    elems.append(Spacer(1, 6))

    # Summary table
    data = [["Field", "Value"],
            ["Case ID", case_id],
            ["Filing Date", datetime.now().strftime("%Y-%m-%d")],
            ["Total Volume", fmt_crypto(total_volume)],
            ["Addresses", str(len(subject_addresses))],
            ["Disposition", "SAR FILING RECOMMENDED"]]
    t = Table(data, colWidths=[2*inch, 4*inch])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0,0), (-1,0), colors.HexColor("#1e1e2e")),
        ("TEXTCOLOR",  (0,0), (-1,0), colors.white),
        ("FONTNAME",   (0,0), (-1,0), "Helvetica-Bold"),
        ("FONTSIZE",   (0,0), (-1,-1), 8),
        ("GRID",       (0,0), (-1,-1), 0.4, colors.grey),
        ("LEFTPADDING",(0,0), (-1,-1), 6),
        ("TOPPADDING", (0,0), (-1,-1), 3),
        ("ROWBACKGROUNDS", (0,1), (-1,-1), [colors.HexColor("#f5f5f5"), colors.white]),
    ]))
    elems.append(t); elems.append(Spacer(1, 10))

    elems.append(Paragraph("Subject Addresses", h1))
    for addr in subject_addresses[:15]:
        elems.append(Paragraph(addr, code))

    elems.append(Paragraph("SAR Narrative", h1))
    for para in narrative.split("\n"):
        if para.strip():
            elems.append(Paragraph(para.strip(), body))
            elems.append(Spacer(1, 2))

    elems.append(Spacer(1, 16))
    elems.append(HRFlowable(width="100%", thickness=1))
    elems.append(Paragraph(
        "CONFIDENTIAL — BSA SAR INFORMATION — DO NOT DISCLOSE  |  "
        "File via: https://bsaefiling.fincen.treas.gov",
        ParagraphStyle("f", parent=S["Normal"], fontSize=7, textColor=colors.grey)
    ))

    doc.build(elems)
    buf.seek(0)
    return buf




